SELECT 
ID_PAIS, 
ID_SOCIEDAD_ORIGEN,
SUM(LITROS_VENDIDOS_NETOS) AS LITROS_TOTALES,
SUM(LITROS_VENDIDOS) AS LITROS_VENDIDOS ,
SUM(LITROS_RECHAZADOS) AS LITROS_RECHAZADOS,
SUM(LITROS_BONIFICADOS) AS LITROS_BONIFICADOS, 
PERIODO
FROM TEST_SILVER.COMERCIAL.SILVER_VENTA_NETA_CORP
WHERE PERIODO >= '202501' 
GROUP BY ID_PAIS, ID_SOCIEDAD_ORIGEN, PERIODO
ORDER BY ID_PAIS, ID_SOCIEDAD_ORIGEN, PERIODO
; 


